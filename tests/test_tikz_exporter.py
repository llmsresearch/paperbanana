@pytest.fixture()
def mock_vlm():
    vlm = AsyncMock()
    vlm.model_name = "gemini-2.0-flash"
    vlm.generate = AsyncMock(
        return_value="```latex\n\\begin{tikzpicture}\n\\node {Test};\n\\end{tikzpicture}\n```"
    )
    return vlm


@pytest.mark.asyncio
async def test_run_produces_tikz_with_header(mock_vlm, tmp_path):
    # Create a dummy image
    from PIL import Image

    img = Image.new("RGB", (100, 100), "white")
    img_path = tmp_path / "test.png"
    img.save(str(img_path))

    agent = TikZExporterAgent(mock_vlm, prompt_dir="prompts")
    result = await agent.run(
        image_path=str(img_path),
        source_context="Test methodology",
        caption="Test caption",
        diagram_type=DiagramType.METHODOLOGY,
        version="0.1.2",
        venue="neurips",
    )

    assert "% Generated by PaperBanana 0.1.2" in result
    assert "% Venue: neurips" in result
    assert "\\begin{tikzpicture}" in result
    assert "\\node {Test};" in result
    mock_vlm.generate.assert_called_once()


