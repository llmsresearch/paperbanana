# ---------------------------------------------------------------------------
# _extract_code
# ---------------------------------------------------------------------------


class TestExtractCode:
    def test_strips_latex_fence(self):
        raw = "```latex\n\\begin{tikzpicture}\n\\end{tikzpicture}\n```"
        assert TikZExporterAgent._extract_code(raw) == (
            "\\begin{tikzpicture}\n\\end{tikzpicture}"
        )

    def test_strips_tikz_fence(self):
        raw = "```tikz\n\\begin{tikzpicture}\n\\end{tikzpicture}\n```"
        assert "\\begin{tikzpicture}" in TikZExporterAgent._extract_code(raw)

    def test_strips_pgfplots_fence(self):
        raw = "```pgfplots\n\\begin{axis}\n\\end{axis}\n```"
        assert "\\begin{axis}" in TikZExporterAgent._extract_code(raw)

    def test_strips_generic_fence(self):
        raw = "```\n\\begin{tikzpicture}\n\\end{tikzpicture}\n```"
        assert "\\begin{tikzpicture}" in TikZExporterAgent._extract_code(raw)

    def test_raw_latex_passthrough(self):
        raw = "\\begin{tikzpicture}\n\\node {A};\n\\end{tikzpicture}"
        assert TikZExporterAgent._extract_code(raw) == raw

    def test_comment_prefix_passthrough(self):
        raw = "% PaperBanana\n\\begin{tikzpicture}\n\\end{tikzpicture}"
        assert TikZExporterAgent._extract_code(raw) == raw

    def test_bare_text_returned_stripped(self):
        raw = "  some unexpected output  "
        assert TikZExporterAgent._extract_code(raw) == "some unexpected output"

    def test_case_insensitive_fence(self):
        raw = "```LATEX\n\\begin{tikzpicture}\n\\end{tikzpicture}\n```"
        assert "\\begin{tikzpicture}" in TikZExporterAgent._extract_code(raw)


